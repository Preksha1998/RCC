public class bitwise_Shift{
	public static void main(String[] args) {
		int n1 = 8 ;
		int n2 = -10;
		//left shift operator
		System.out.println("n1 << 2 " + (n1 << 2));

		//right shift operator
		System.out.println("n2 >> 2 " + (n2 >> 2));

		//unsigned right shift operator
		System.out.println("n2 >>> 2 " + (n2 >>> 2));	

	}
}